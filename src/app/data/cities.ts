// Calculate distance between two coordinates using Haversine formula
export function calculateDistance(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 3959; // Earth's radius in miles
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLng = (lng2 - lng1) * Math.PI / 180;
  const a =
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLng/2) * Math.sin(dLng/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c;
}

// Find city by display name or partial match
export function findCity(query: string): City | undefined {
  const normalizedQuery = query.toLowerCase().trim();
  return US_CITIES.find(
    city => city.displayName.toLowerCase() === normalizedQuery ||
            city.name.toLowerCase() === normalizedQuery
  );
}

// Get nearby larger cities sorted by population (for news fallback)
export function getNearbyCities(cityName: string, maxDistance: number = 50, minPopulation: number = 100000): City[] {
  const sourceCity = findCity(cityName);
  if (!sourceCity) return [];

  return US_CITIES
    .filter(city => {
      if (city.displayName === sourceCity.displayName) return false;
      if (city.population < minPopulation) return false;
      const distance = calculateDistance(sourceCity.lat, sourceCity.lng, city.lat, city.lng);
      return distance <= maxDistance;
    })
    .map(city => ({
      ...city,
      distance: calculateDistance(sourceCity.lat, sourceCity.lng, city.lat, city.lng)
    }))
    .sort((a, b) => b.population - a.population) // Sort by population (larger first)
    .slice(0, 5); // Return top 5 nearby cities
}

// Search cities for autocomplete
export function searchCities(query: string, limit: number = 10): City[] {
  if (!query || query.length < 2) return [];

  const normalizedQuery = query.toLowerCase().trim();

  // First, find exact matches at the start of city name
  const startsWithMatches = US_CITIES.filter(city =>
    city.name.toLowerCase().startsWith(normalizedQuery) ||
    city.displayName.toLowerCase().startsWith(normalizedQuery)
  );

  // Then, find partial matches
  const containsMatches = US_CITIES.filter(city => {
    const inStartsWith = startsWithMatches.some(m => m.displayName === city.displayName);
    if (inStartsWith) return false;
    return city.name.toLowerCase().includes(normalizedQuery) ||
           city.displayName.toLowerCase().includes(normalizedQuery);
  });

  // Combine and sort by population (larger cities first)
  const combined = [...startsWithMatches, ...containsMatches]
    .sort((a, b) => b.population - a.population);

  return combined.slice(0, limit);
}
